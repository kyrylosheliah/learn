
#########################################################################
# 1. Згенерувати цілочисловий вектор X, що складається з 100 випадкових
# чисел, що підкоряються нормальному розподілу з математичним сподіванням
# 50 і середнім квадратичним відхиленням 10.
#########################################################################
# Встановлюємо seed для відтворюваності результатів
set.seed(123)
# Задаємо параметри нормального розподілу
mean_value <- 50
sd_value <- 10
n <- 100
# Генеруємо вектор X
X <- rnorm(n, mean = mean_value, sd = sd_value)
# Округлюємо значення до найближчих цілих чисел
X <- round(X)
# Виводимо результат
print(X)

#########################################################################
# 2. Визначити максимальний, мінімальний елементи і їхні індекси у векторі
# X. Визначити середнє геометричне й медіану значень вектора X.
#########################################################################
# Визначаємо мінімальний елемент і його індекс
min_index <- which.min(X)
min_value <- X[min_index] # min(X)
max_index <- which.max(X)
max_value <- X[max_index] # max(X)
# Визначаємо середнє геометричне значень вектора X
geometric_mean <- mean(X)
# Визначаємо медіану значень вектора X
median_value <- median(X)
# Виводимо результати
cat("Максимальний елемент:", max_value, "з індексом:", max_index, "\n")
cat("Мінімальний елемент:", min_value, "з індексом:", min_index, "\n")
cat("Середнє геометричне значень вектора X:", geometric_mean, "\n")
cat("Медіана значень вектора X:", median_value, "\n")

#########################################################################
# 3. Згенерувати вектор X1, що складається з елементів вектора X, менших
# 40 або більших 60.
#########################################################################
# Створюємо вектор X1, що містить елементи вектора X менші за 40 або
# більші за 60
X1 <- X[X < 40 | X > 60]
# Виводимо результат
print(X1)

#########################################################################
# 4. Побудувати графіки тригонометричних функцій. Для кожної функції
# побудувати 5 графіків з різними типами\стилями ліній функції plot().
#########################################################################
# Встановлюємо послідовність значень для осі X
x <- seq(0, 2*pi, length.out = 10)
# Створюємо вектор функцій для побудови графіків
trig_functions <- list(sin, cos, tan, function(x) 1/tan(x), function(x) 1/cos(x))
function_names <- c("sin(x)", "cos(x)", "tan(x)", "cot(x)", "sec(x)")
line_types <- c("solid", "dashed", "dotted", "dotdash", "longdash")
palette <- c("red", "green", "blue", "yellow", "purple")
line_widths <- c(1,2,3,4,5)
point_shapes <- c(1,2,3,4,5)
point_sizes <- c(1,2,3,4,5)
# Встановлюємо параметри для побудови графіків в одному вікні
par(mfrow = c(5, 1), mar = c(4, 4, 2, 1))
# Побудова графіків для кожної функції з різними типами ліній
for (i in 1:length(trig_functions)) {
  plot(x, trig_functions[[i]](x), type = "b", ylab = function_names[i], xlab = "x",
    col = palette[i],
    lty = line_types[i],
    lwd = line_widths[i],
    pch = point_shapes[i],
    cex = point_sizes[i])
}

